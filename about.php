<p>My name is Dr. Michelle Rudolph-Lilith, and I am an independent senior research scientist in the UNIC lab 
(Unité de Neurosciences, Information et Complexité) of the French Federal Research Agency (CNRS). </p>

<p>My educational background, and true passion, resides in theoretical physics, specifically effective and 
quantum field theories and mathematics. After a decade-long detour through the, at times, abysmal realms of theoretical 
neuroscience, where my research focused on the computational and mathematical modelling of cortical activity 
seen in the awake brain, my current research interests include graph theory, number theory and discrete (effinite) 
mathematics along with its applications and philosophical implications. </p>

<p>Specifically, my current research projects and interests include the
<ul style="list-style-type:disc;margin-left:20px;margin-right:20px;">
  <li>development of a rigorous mathematical framework for <i>Operator Graph Theory</i> and its application to the description of finite graphs</li>
  <li>search for solutions to a variety of number-theoretical problems and conjectures, such as the Gauss's Circle Problem and Riemann Hypothesis</li>
  <li>development of a rigorous discrete (finite or effinite) mathematical framework for the description of reality and assessment of the philosophical implications of a rejection of the Cantorian real number concept</li>
  <li>development of a dynamical quantum theory of space and time based on <sc>von Staudt</sc>'s projective geometrical construction of coordinate spaces</li>
  <li>search for an embedding of quantum-theoretical aspects into the concept of self-awareness and consciousness beyond <sc>Kant</sc></li>
</ul></p>

