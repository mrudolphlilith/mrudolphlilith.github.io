<h3><span>About this Book</span></h3>

<p><em>Neuronal Noise</em> combines experimental, theoretical and computational results to show how noise is inherent to neuronal 
activity, and how noise can be important for neuronal computations.  The book covers many aspects of noise in neurons, 
with an emphasis on the largest source of noise: synaptic noise. It provides students and young researchers with an 
overview of the important methods and concepts that have emerged from research in this area. It also provides the 
specialist with a summary of the large body of sometimes contrasting experimental data, and different theories proposed 
to explore the computational power that various forms of "noise" can confer to neurons.</p>
		
