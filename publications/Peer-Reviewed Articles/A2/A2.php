<h3><span>Abstract</span></h3>

<p>The structure of the algebra of gauge invariant differential forms built from SU(3)-gauge potentials as well
as (Grassmann-algebra-valued) quark and antiquark fields is discussed. The relevance for one-flavour
chromodynamics is outlined.</p>
