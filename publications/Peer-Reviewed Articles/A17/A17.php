<h3><span>Abstract</span></h3>

<p>The activation of the electroencephalogram (EEG) is paralleled with an increase in the firing rate of cortical neurons,
but little is known concerning the conductance state of their membrane, and its impact on their integrative properties.
Here, we combined <em>in vivo</em> intracellular recordings with computational models to investigate EEG-activated states induced
by stimulation of the brainstem ascending arousal system. Electrical stimulation of the pedonculopontine tegmental
(PPT) nucleus produced long-lasting (approx. 20 sec) periods of desynchronized EEG activity similar to the EEG of
awake animals. Intracellularly, PPT stimulation locked the membrane into a depolarized state, similar to the up-states
seen during deep anesthesia. During these EEG-activated states, however, the input resistance was higher than during
up-states. Conductance measurements were performed using different methods, which all indicate that EEG-activated
states were associated with a synaptic activity dominated by inhibitory conductances. These results were confirmed
by computational models of reconstructed pyramidal neurons constrained by the corresponding intracellular recordings
These models indicate that, during EEG-activated states, neocortical neurons are in a high-conductance state consistent
with a stochastic integrative mode. The amplitude and timing of somatic EPSPs was nearly independent of the position of
the synapses in dendrites, suggesting that EEG-activated states are compatible with coding paradigms involving the precise
timing of synaptic events.</p>
