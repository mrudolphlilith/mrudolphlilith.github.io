<h3><span>Abstract</span></h3>

<p>The central coefficients of powers of certain polynomials with arbitrary degree in $x$ form an important family 
of integer sequences. Although various recursive equations addressing these coefficients do exist, no explicit 
analytic representation has yet been proposed. In this article, we present an explicit form of the integer 
sequences of central multinomial coefficients of polynomials of even degree in terms of finite sums over Dirichlet 
kernels, hence linking these sequences to discrete $n$th-degree Fourier series expansions. The approach utilizes 
the diagonalization of circulant Boolean matrices, and is generalizable to all multinomial coefficients of 
certain polynomials with even degree, thus forming the base for a new family of combinatorial identities.</p>
