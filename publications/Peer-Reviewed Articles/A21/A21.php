<h3><span>Abstract</span></h3>

<p>Systems driven by Poisson-distributed quantal inputs can be described as 'shot noise' stochastic processes. 
This formalism can apply to neurons which receive a large number of Poisson-distributed synaptic inputs of 
similar quantal size. However, the presence of temporal correlations between these inputs destroys their 
quantal nature, and such systems can no longer be described by classical shot noise processes. Here, we 
show that explicit expressions for various statistical properties, such as the amplitude distribution and 
the power spectral density, can be deduced and investigated as functions of the correlation between input 
channels. The monotonic behavior of these expressions allows an one-to-one relation between temporal 
correlations and the statistics of fluctuations. Multi-channel shot noise processes, therefore, open a 
way to deduce correlations in input patterns by analyzing fluctuations in experimental systems. We 
discuss applications such as detecting correlations in networks of neurons from intracellular recordings 
of single neurons.</p>
