<h3><span>Abstract</span></h3>

<p><em>In vivo</em>, <em>in vitro</em> and computational studies were used to investigate the impact of the synaptic
background activity observed in neocortical neurons <em>in vivo</em>. We simulated background activity <em>in vitro</em>
using two stochastic Ornstein-Uhlenbeck processes describing glutamatergic and GABAergic synaptic
conductances, which were injected into a cell in real time using the dynamic clamp technique. With
parameters chosen to mimic in vivo conditions, layer 5 rat prefrontal cortex cells recorded <em>in vitro</em>
were depolarized by about 15 mV, their membrane fluctuated with a S.D. of about 4 mV, their input
resistances decreased five-fold, their spontaneous firing had a high coefficient of variation and
an average firing rate of about 5-10 Hz. Brief changes in the variance of the
alpha-amino-3-hydroxy-5-methyl-4-isoxazolepropionic acid (AMPA) synaptic conductance fluctuations
induced time-locked spiking without significantly changing the average membrane potential of the
cell. These transients mimicked increases in the correlation of excitatory inputs. Background
activity was highly effective in modulating the firing-rate/current curve of the cell: the variance
of the simulated gamma-aminobutyric acid (GABA) and AMPA conductances individually set the
input/output gain, the mean excitatory and inhibitory conductances set the working point,
and the mean inhibitory conductance controlled the input resistance. An average ratio of inhibitory
to excitatory mean conductances close to 4 was optimal in generating membrane potential fluctuations
with high coefficients of variation. We conclude that background synaptic activity can dynamically
modulate the input/output properties of individual neocortical neurons <em>in vivo</em>.</p>
