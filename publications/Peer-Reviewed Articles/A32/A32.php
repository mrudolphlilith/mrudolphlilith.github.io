<h3><span>Abstract</span></h3>

<p>In the past two decades, significant advances have been made in understanding the structural and functional properties of biological networks, 
via graph-theoretic analysis. In general, most graph-theoretic studies are conducted in the presence of serious uncertainties, such as major 
undersampling of the experimental data. In the specific case of neural systems, however, a few moderately robust experimental reconstructions 
have been reported, and these have long served as fundamental prototypes for studying connectivity patterns in the nervous system. In this paper, 
we provide a comparative analysis of these "historical" graphs, both in their directed (original) and symmetrized (a common preprocessing step) 
forms, and provide a set of measures that can be consistently applied across graphs (directed or undirected, with or without self-loops). 
We focus on simple structural characterizations of network connectivity and find that in many measures, the networks studied are captured 
by simple random graph models. In a few key measures, however, we observe a marked departure from the random graph prediction. Our results 
suggest that the mechanism of graph formation in the networks studied is not well captured by existing abstract graph models in their first- 
and second-order connectivity.</p>

