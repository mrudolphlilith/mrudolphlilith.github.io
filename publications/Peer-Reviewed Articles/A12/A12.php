<h3><span>Abstract</span></h3>

<p><em>In vivo</em> recordings have shown that the discharge of cortical neurons is often highly variable and can
have statistics similar to a Poisson process with a coefficient of variation around unity. To investigate the
determinants of this high variability, we analyzed the spontaneous discharge of Hodgkin-Huxley type models of
cortical neurons, in which <em>in vivo</em> like synaptic background activity was modeled by random release events at
excitatory and inhibitory synapses. By using compartmental models with active dendrites, or single compartment
models with fluctuating conductances and fluctuating currents, we found that a high discharge variability was
always paralleled with a high-conductance state, while some active and passive cellular properties had only a
minor impact. Furthermore, a balance between excitation and inhibition was not a necessary condition for high
discharge variability. We conclude that the fluctuating high-conductance state caused by the ongoing activity
in the cortical network <em>in vivo</em> may be viewed as a natural determinant of the highly variable discharges of
these neurons.</p>
