<h3><span>Abstract</span></h3>

<p>In this computational study, we investigated (i) the functional importance of correlated basal ganglia 
(BG) activity associated with Parkinson's disease (PD) motor symptoms by analysing the effects of 
globus pallidus internum (GPi) bursting frequency and synchrony on a thalamocortical (TC) relay neuron, 
which received GABAergic projections from this nucleus; (ii) the effects of subthalamic nucleus (STN) deep 
brain stimulation (DBS) on the response of the TC relay neuron to synchronized GPi oscillations; and (iii) 
the functional basis of the inverse relationship that has been reported between DBS frequency and stimulus 
amplitude, required to alleviate PD motor symptoms [A. L. Benabid et al. (1991)Lancet, 337, 403-406]. The 
TC relay neuron selectively responded to and relayed synchronized GPi inputs bursting at a frequency 
located in the range 2-25 Hz. Input selectivity of the TC relay neuron is dictated by low-threshold calcium 
current dynamics and passive membrane properties of the neuron. STN-DBS prevented the TC relay neuron from 
relaying synchronized GPi oscillations to cortex. Our model indicates that DBS alters BG output and input 
selectivity of the TC relay neuron, providing an explanation for the clinically observed inverse relationship 
between DBS frequency and stimulus amplitude.</p>

