<h3><span>Abstract</span></h3>

<p>Synaptic noise due to intense network activity can significantly impact on the electrophysiological properties of
individual neurons. This is the case for the cerebral cortex, where ongoing activity leads to strong barrages of
synaptic inputs, which act as the main source of synaptic noise impacting on neuronal dynamics. Here, we characterize
the subthreshold behavior of neuronal models in which synaptic noise is represented by either additive or
multiplicative noise, described by Ornstein-Uhlenbeck processes. We derive and solve the Fokker-Planck equation
for this system, which describes the time evolution of the probability density function for the membrane
potential. We obtain an analytic expression for the membrane potential distribution at steady-state and
compare this analytic expression with the subthreshold activity obtained in Hodgkin-Huxley type models
with stochastic synaptic inputs. The differences between multiplicative and additive noise models suggest
that multiplicative noise is adequate to describe the high-conductance states similar to <em>in vivo</em> conditions.
Because the steady-state membrane potential distribution is easily obtained experimentally, this approach
provides a possible method to estimate the mean and variance of synaptic conductances in real neurons.</p>
