<h3><span>Abstract</span></h3>

<p>Many hardware-based solutions now exist for the simulation of bio-like neural networks. Less conventional 
than software-based systems, these types of simulators generally combine digital and analog forms of computation. 
In this paper we present a mixed hardware-software platform, specifically designed for the simulation of spiking 
neural networks, using conductance-based models of neurons and synaptic connections with dynamic adaptation 
rules (Spike-Timing-Dependent Plasticity). The neurons and networks are configurable, and are computed in 
'biological real time' by which we mean that the difference between simulated time and simulation time is 
guaranteed lower than 50 &mu;s. After presenting the issues and context involved in the design and use of 
hardware-based spiking neural networks, we describe the analog neuromimetic integrated circuits which form 
the core of the platform. We then explain the organization and computation principles of the modules within 
the platform, and present experimental results which validate the system. Designed as a tool for computational 
neuroscience, the platform is exploited in collaborative research projects together with neurobiology and computer 
science partners.</p>
	