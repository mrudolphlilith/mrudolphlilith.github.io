<h3><span>Abstract</span></h3>

<p>In cortical neurons, synaptic 'noise' is caused by the nearly-random release of thousands of synapses. Few methods are presently
available to analyze synaptic noise and deduce properties of the underlying synaptic inputs. We focus here on the power spectral
density (PSD) of several models of synaptic noise. We examine different classes of analytically-solvable kinetic models for
synaptic currents, such as the 'delta kinetic models', which use Dirac delta functions to represent the activation of the
ion channel. We first show that, for this class of kinetic models, one can obtain an analytic expression for the PSD of
the total synaptic conductance and derive equivalent stochastic models with only a few variables. This yields a method for
constraining models of synaptic currents by analyzing voltage-clamp recordings of synaptic noise. Second, we show that a
similar approach can be followed for the PSD of the the membrane potential (Vm) through an effective-leak approximation.
Third, we show that this approach is also valid for inputs distributed in dendrites. In this case, the frequency-scaling
of the Vm PSD is preserved, suggesting that this approach may be applied to intracellular recordings of real neurons. In
conclusion, using simple mathematical tools, we show that Vm recordings can be used to constrain kinetic models of synaptic
currents, as well as to estimate equivalent stochastic models. This approach therefore provides a direct link between
intracellular recordings <em>in vivo</em> and the design of models consistent with the dynamics and spectral structure of
synaptic noise.</p>
