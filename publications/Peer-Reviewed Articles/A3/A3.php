<h3><span>Abstract</span></h3>

<p>The functional integral of the massless Schwinger model in (1+1) dimensions is reduced to an integral in terms
of local gauge invariant quantities. It turns out that this approach leads to a natural bosonisation scheme,
yielding, in particular the famous 'bosonisation rule' and giving some deeper insight into the nature of the
bosonisation phenomenon. As an application, the chiral anomaly is calculated within this formulation.</p>
