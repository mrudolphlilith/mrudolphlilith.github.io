<h3><span>Abstract</span></h3>

<p>Spike-timing dependent plasticity (STDP) is a form of associative synaptic modification which 
depends on the respective timing of pre- and post-synaptic spikes. The biophysical mechanisms 
underlying this form of plasticity are currently not known. We present here a biophysical model 
which captures the characteristics of STDP, such as its frequency dependency, and the effects of 
spike pair or spike triplet interactions. We also make links with other well-known plasticity 
rules. A simplified phenomenological model is also derived, which should be useful for fast 
numerical simulation and analytical investigation of the impact of STDP at the network level.</p>

