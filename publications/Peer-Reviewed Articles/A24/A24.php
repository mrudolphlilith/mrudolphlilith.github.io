<h3><span>Abstract</span></h3>

<p>Intracellular recordings of cortical neurons in awake cat and monkey show a depolarized 
state, sustained firing and intense subthreshold synaptic activity. It is not known what 
conductance dynamics underlies such activity, and how neurons process information in such 
highly stochastic states. Here, we combine intracellular recordings in awake and naturally 
sleeping cats with computational models to investigate subthreshold dynamics of conductances 
and how conductance dynamics determine spiking activity. We show that during both wakefulness 
and the "up-states" of natural slow-wave sleep, membrane potential activity stems from a 
diversity of combinations of excitatory and inhibitory synaptic conductances, with dominant 
inhibition in most of the cases. Inhibition also provides the largest contribution to membrane 
potential fluctuations. Computational models predict that in such inhibition-dominant states, 
spikes are preferentially evoked by a drop of inhibitory conductance, and that its signature 
is a transient drop of membrane conductance prior to the spike. This pattern of conductance 
change is indeed observed in estimates of spike-triggered averages of synaptic conductances 
during wakefulness and slow-wave sleep up-states. These results show that activated states 
are defined by diverse combinations of excitatory and inhibitory conductances with pronounced 
inhibition, and that the dynamics of inhibition is particularly effective on spiking, 
suggesting an important role for inhibitory processes in both conscious and unconscious 
cortical states.</p>

