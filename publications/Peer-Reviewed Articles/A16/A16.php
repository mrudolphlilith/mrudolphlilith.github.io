<h3><span>Abstract</span></h3>

<p>In neocortical neurons, network activity can activate a large number of synaptic inputs, resulting in highly irregular
subthreshold membrane potential (Vm) fluctuations, commonly called 'synaptic noise'. This activity contains information
about the underlying network dynamics, but it is not easy to extract network properties from such complex and irregular
activity. Here, we propose a method to estimate properties of network activity from intracellular recordings and test
this method using theoretical and experimental approaches. The method is based on the analytic expression of the
subthreshold Vm distribution at steady-state in conductance-based models. Fitting this analytic expression to Vm
distributions obtained from intracellular recordings provides estimates of the mean and variance of excitatory and
inhibitory conductances. We test the accuracy of these estimates against computational models of increasing complexity.
We also test the method using dynamic-clamp recordings of neocortical neurons in vitro. By using an on-line analysis
procedure, we show that the measured conductances from spontaneous network activity can be used to recreate artificial
states equivalent to real network activity. This approach should be applicable to intracellular recordings during
different network states <em>in vivo</em>, providing a characterization of the global properties of synaptic conductances
and possible insight on the underlying network mechanisms.</p>

