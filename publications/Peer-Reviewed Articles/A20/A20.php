<h3><span>Abstract</span></h3>

<p>Event-driven simulation strategies were proposed recently to simulate integrate- and-fire (IF) type neuronal models. 
These strategies can lead to computationally efficient algorithms for simulating large-scale networks of neurons, 
but most importantly, such approaches are more precise than traditional clock-driven numerical integration 
approaches because the timing of spikes is treated exactly. The drawback of such event-driven methods is that, 
in order to be efficient, the membrane equations must be solvable analytically, or at least provide simple 
analytic approximations for the state variables describing the system. This requirement prevents, in general, 
the use of conductance-based synaptic interactions within the framework of event-driven simulations and, 
thus, the investigation of network paradigms where synaptic conductances are important. We propose here a 
number of extensions of the classical leaky IF neuron model involving approximations of the membrane equation 
with conductance-based synaptic current, which lead to simple analytic expressions for the membrane state which, 
therefore, can be used in the event-driven framework. These conductance-based IF models (gIF models) are compared 
to commonly used models, such as the leaky IF model or biophysical models in which conductances are explicitly 
integrated. All models are compared with respect to various spiking response properties in the presence of synaptic 
activity, such as the spontaneous discharge statistics, the temporal precision in resolving synaptic inputs, and 
gain modulation under <em>in vivo</em>-like synaptic bombardment. Being based on the passive membrane equation with 
fixed-threshold spike generation, the proposed gIF models are situated in between leaky IF and biophysical
models, but are much closer to the latter with respect to their dynamic behavior and response characteristics,
while still being nearly as computationally efficient as simple IF neuron models. gIF models should, therefore,
provide a useful tool for efficient and precise simulation of large-scale neuronal networks with realistic,
conductance-based synaptic interactions.</p>	
