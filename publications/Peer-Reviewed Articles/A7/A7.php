<h3><span>Abstract</span></h3>

<p>Neocortical pyramidal neurons <em>in vivo</em> are subject to an intense synaptic background activity which has a
significant impact on various electrophysiological properties and dendritic integration. Using detailed
biophysical models of a morphologically-reconstructed neocortical pyramidal neuron, in which synaptic
background activity was simulated according to recent measurements in cat parietal cortex <em>in vivo</em>, we
show that the responsiveness of the cell to additional periodic subthreshold stimuli can be significantly
enhanced through mechanisms similar to stochastic resonance. We compare several paradigms leading
to stochastic resonance-like behavior, such as varying the strength or the correlation in the background
activity. Interestingly, a new type of resonance-like behavior was obtained when the correlation
was varied, in which case the responsiveness is sensitive to the statistics rather than the strength
of the 'noise'. We suggest that this type of resonance may be relevant to information processing in
the cerebral cortex.</p>
