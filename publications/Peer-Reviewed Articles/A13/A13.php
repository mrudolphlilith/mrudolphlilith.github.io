<h3><span>Abstract</span></h3>

<p>Do cortical neurons operate as integrators or as coincidence detectors? Despite the importance of this question,
no definite answer has been given yet, because each of these two views can find its own experimental support.
Here we investigated this question using models of morphologically-reconstructed neocortical pyramidal neurons
under <em>in-vivo</em>-like conditions. In agreement with experiments we find that the cell is capable of operating
in a continuum between coincidence detection and temporal integration, depending on the characteristics of
the synaptic inputs. Moreover, the presence of synaptic background activity at a level comparable to intracellular
measurements <em>in vivo</em> can modulate the operating mode of the cell, and act as a switch between temporal integration
and coincidence detection. These results suggest that background activity can be viewed as an important determinant
of the integrative mode of pyramidal neurons. Thus, background activity not only sharpens cortical responses
but it can also be used to tune an entire network between integration and coincidence detection modes.</p>
