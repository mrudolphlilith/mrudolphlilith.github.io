<h3><span>Abstract</span></h3>

<p>A procedure for reducing the functional integral of QED to an integral over bosonic gauge
invariant fields is presented. Next, a certain averaging method for this integral, giving
a tractable effective quantum field theory, is proposed. Finally, the current-current propagator
and the chiral anomaly are calculated within this new formulation. These results are part of our
programme of analyzing gauge theories with fermions in terms of local gauge invariants.</p>
