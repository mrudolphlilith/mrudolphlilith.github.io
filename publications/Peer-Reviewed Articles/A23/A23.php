<h3><span>Abstract</span></h3>

<p>In awake animals, the cerebral cortex displays an 'activated' state, with distinct 
characteristics compared to other states like slow-wave sleep or anesthesia. These characteristics 
include a sustained depolarized membrane potential (Vm) and irregular firing activity. In the present 
paper, we evaluate our understanding of cortical activated states from a computational neuroscience 
point of view. We start by reviewing the electrophysiological characteristics of activated cortical 
states based on recordings and analysis performed in awake cat association cortex. These analyses 
show that cortical activity is characterized by an apparent Poisson-distributed stochastic dynamics, 
both at the single-cell and population levels, and that single cells display a high-conductance 
state dominated by inhibition. We next overview computational models of the 'awake' cortex, and 
perform the same analyses in models. Many properties identified experimentally are indeed reproduced 
by models, such as depolarized Vm, irregular firing with apparent Poisson statistics, and the 
determinant role of inhibitory fluctuations on spiking. However, other features are not well 
reproduced, such as firing statistics and the conductance state of the membrane, suggesting that 
the network state displayed by models is not entirely correct. We also show how networks can approach 
a correct conductance state, suggesting ways by which future models will generate activity fully 
consistent with experimental data.</p>
