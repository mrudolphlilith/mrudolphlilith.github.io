<h3><span>Abstract</span></h3>

<p>We reduce the functional integral of quantum electrodynamics to an integral containing only local gauge
invariant quantities. The set of (bosonic) invariants contains bilinear combinations of the spinor
field and a real-valued covector field.</p>
