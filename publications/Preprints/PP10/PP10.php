<h3><span>Abstract</span></h3>

<p>We propose an approach to calculate the critical percolation threshold for finite-sized Erd&#337;s-R&eacute;nyi digraphs 
using minimal Hamiltonian cycles. We obtain an analytically exact result, valid non-asymptotically for all graph 
sizes, which scales in accordance with results obtained for infinite random graphs using the emergence of a giant 
connected component as marking the percolation transition. Our approach is general and can be applied to all graph 
models for which an algebraic formulation of the adjacency matrix is available.</p>
