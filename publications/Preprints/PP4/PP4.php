<h3><span>Abstract</span></h3>

<p>This is the written version of a short talk given at the University of Leipzig in December 1998.
It reviews some general aspects of string theory from the viewpoint of the search for an unifying
theory. Here, special emphasis lies on the motivation to consider string theory not only as the
leading candidate for the unification of gravity and the other fundamental forces of nature, but
also as a possible step towards a new understanding of nature and its description within the
framework of physical models. Without going into details, some recent developments, including
duality symmetries and the appearance of M-theory, are reviewed.</p>
