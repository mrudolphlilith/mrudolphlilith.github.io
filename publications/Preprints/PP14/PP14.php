<h3><span>Abstract</span></h3>

<p>Is it possible to draw a circle in Manhattan, using only its discrete network of streets and boulevards? 
In this study, we will explore the construction and properties of circular paths on an integer lattice, a 
discrete space where the distance between two points is not governed by the familiar Euclidean metric, but 
the Manhattan or taxicab distance, a metric linear in its coordinates. In order to achieve consistency with 
the continuous ideal, we need to abandon Euclid's very original definition of the circle in favour of a 
parametric construction. Somewhat unexpectedly, we find that the Euclidean circle's defining constant $\pi$ 
can be recovered in such a discrete setting.</p>
