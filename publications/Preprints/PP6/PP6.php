<h3><span>Abstract</span></h3>

<p>Different analytic expressions for the membrane potential distribution of membranes subject to synaptic noise have
been proposed, and can be very helpful to analyze experimental data. However, all of these expressions are either
approximations or limit cases, and it is not clear how they compare, and which expression should be used in a given
situation. In this note, we provide a comparison of the different approximations available, with an aim to delineate
which expression is most suitable for analyzing experimental data.</p>
