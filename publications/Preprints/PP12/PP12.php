<h3><span>Abstract</span></h3>

<p>We investigate general properties of number sequences which allow explicit representation in terms of products. 
We find that such sequences form whole families of number sequences sharing similar recursive identities. 
Restricting to the cosine of fractional angles, we then study the special case of the family of $k$-generalized 
Fibonacci numbers, and present general recursions and identities which link these sequences.</p>
