<h3><span>Abstract</span></h3>

<p>In the past two decades, significant advances have been made in understanding the structural and 
functional properties of biological networks, via graph-theoretic analysis. In general, most graph-theoretic 
studies are conducted in the presence of serious uncertainties, such as major undersampling of the 
experimental data. In the specific case of neural systems, however, a few moderately robust experimental 
reconstructions do exist, and these have long served as fundamental prototypes for studying connectivity 
patterns in the nervous system. In this paper, we provide a comparative analysis of these "historical" 
graphs, both in (unmodified) directed and (often symmetrized) undirected forms, and focus on simple 
structural characterizations of their connectivity. We find that in most measures the networks studied 
are captured by simple random graph models; in a few key measures, however, we observe a marked departure 
from the random graph prediction. Our results suggest that the mechanism of graph formation in the networks 
studied is not well-captured by existing abstract graph models, such as the small-world or scale-free graph.</p>
