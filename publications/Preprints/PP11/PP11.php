<h3><span>Abstract</span></h3>

<p>We propose an explicit representation of central $(2k+1)$-nomial coefficients in terms of finite 
sums over trigonometric constructs. The approach utilizes the diagonalization of circulant boolean 
matrices and is generalizable to all $(2k+1)$-nomial coefficients, thus yielding a new family of 
combinatorical identities.</p>
