<h3><span>Abstract</span></h3>

<p>Digital circles not only play an important role in various technological settings, but also provide a lively playground for more 
fundamental number-theoretical questions. In this paper, we present a new recursive algorithm for the construction of digital circles 
on the integer lattice $\mathbb{Z}^2$, which makes sole use of the signum function. By briefly elaborating on the nature of 
discretization of circular paths, we then find that this algorithm recovers, in a space endowed with $\ell^1$-norm, the defining 
constant $\pi$ of a circle in $\mathbb{R}^2$.</p>
