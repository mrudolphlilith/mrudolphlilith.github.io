<h3><span>Abstract</span></h3>

<p>The number of connected components and the size of the largest connected component are studied under node 
and edge removal in the connectivity graph of the <em>C. elegans</em> nervous system. By studying the two 
subgraphs - the directed graph of chemical synapses and the undirected graph of electrical junctions - we 
observe that adding a small number of undirected edges dramatically reduces the number of components in the 
complete graph. Under random node and edge removal, the <em>C. elegans</em> graph displays a remarkable 
structural robustness. We then compare these results with the vulnerability of a number of canonical 
graph models.</p>
