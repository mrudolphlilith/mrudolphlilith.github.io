<h3><span>Abstract</span></h3>

<p>Recent <em>in vivo</em> intracellular measurements indicate that cortical neurons operate in a high-conductance
state mainly caused by intense network activity. Biophysical models of morphologically-reconstructed neocortical
neurons with thousands of random synaptic events successfully reproduce intracellular measurements and the high
discharge variability. Here we compare several classes of simplified models. Experimental findings are reproduced
when the high-conductance component is explicitly taken into account. In contrast to integrate-and-fire models,
the high discharge variability does not depend on the balance between excitation and inhibition. We suggest
that basic electrophysiological properties and irregular activity of cortical neurons <em>in vivo</em> can be optimally
captured by high-conductance models.</p>
