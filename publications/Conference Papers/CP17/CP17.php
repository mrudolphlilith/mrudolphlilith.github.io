<h3><span>Abstract</span></h3>

<p>We present a new way to model the response of an electrode to an injected current. The electrode is represented by an
unknown complex linear circuit, characterized by a kernel which we determine by injecting a noisy current. We show both
in simulations and experiments that, when applied to a full recording setup (including acquisition board and amplifier),
the method captures not only the characteristics of the electrode, but also those of all the devices between the
computer and the tip of the electrode, including filters and the capacitance neutralization circuit on the amplifier.
Simulations show that the method allows correct predictions of the response of complex electrode models. Finally, we
successfully apply the technique to challenging intracellular recording situations in which the voltage across the
electrode during injection needs to be subtracted from the recording, in particular conductance injection with the
dynamic clamp protocol. We show in numerical simulations and confirm with experiments that the method performs well
in cases when both bridge recording and recording in discontinuous mode (DCC) exhibit artefacts.</p>
