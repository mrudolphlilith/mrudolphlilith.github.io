<h3><span>Abstract</span></h3>

<p>We present some results, which are part of our programme of analyzing gauge theories with fermions in
terms of local gauge invariant fields. In a first part the classical Dirac--Maxwell system is discussed.
Next we develop a procedure which leads to a reduction of the functional integral to an integral over
(bosonic) gauge invariant fields. We apply this procedure to the case of QED and the Schwinger model.
In a third part we go some steps towards an analysis of the considered models. We construct effective
(quantum) field theories which can be used to calculate vacuum expectation values of physical quantities.</p>
