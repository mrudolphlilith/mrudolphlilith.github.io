<h3><span>Abstract</span></h3>

<p>Neocortical neurons <em>in vivo</em> operate in a high-conductance state which may affect the dynamics of dendritic
integration, but this aspect remains only barely characterized. We investigated this problem by using biophysical
models of morphologically-reconstructed neocortical pyramidal neurons in which isolated and paired synaptic events
were studied according to their dendritic location. We show that during active states, there is an ongoing dynamics
of randomly occurring forward- and back-propagating dendritic action potentials. This dynamics determinantly
impacts on how individual or paired synaptic events interact, leading to a type of integrative behavior which
is different from classical models of dendritic integration.</p>
