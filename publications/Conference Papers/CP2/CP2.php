<h3><span>Abstract</span></h3>

<p>We present some results, which are part of our programme of analyzing gauge theories in terms of physical observables
(gauge invariants). For applications of these ideas to theories of gauge fields interacting with fermionic matter
fields we refer to [1]-[4]. In the present contribution we show that the classical Dirac-Maxwell system can
be reformulated in a spin rotation covariant way in terms of invariants. Next, we demonstrate that the functional
integral of QED can be reduced to an integral over physical degrees of freedom described by an analogous set
of invariants. The latter invariants are, however, Grassmann algebra valued. Finally, we outline an application.
We show how to calculate the anomaly in the 2-dimensional Schwinger model within our formulation.</p>
