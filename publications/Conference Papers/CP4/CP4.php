<h3><span>Abstract</span></h3>

<p>Neocortical pyramidal neurons <em>in vivo</em> are subject to an intense synaptic background activity which may
significantly impact on dendritic integration, but this aspect is largely unexplored. Here we use computational
models of morphologically-reconstructed pyramidal neurons, in which synaptic background activity was simulated
according to recent measurements in cat parietal cortex. We show that background activity markedly enhances
voltage attenuation, which results in a relative electrotonic 'isolation' of different dendritic segments.
On the other hand, the active propagation of action potentials in dendrites is minimally affected. The
consequence is that inputs are integrated locally and their impact on the soma is independent on their
position in the dendritic tree. We conclude that background activity sets up a dynamics of dendritic
integration which is radically different compared to quiescent states.</p>
