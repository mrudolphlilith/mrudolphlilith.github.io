<h3><span>Abstract</span></h3>

<p>To reconstruct synaptic background activity, we combined computational models with intracellular recordings
<em>in vivo</em>. A new conductance analysis method applied to intracellular recordings in the somatosensory
cortex of anesthetized rats revealed mirror changes of excitation and inhibition during the up- and down-states
of slow waves, but concerted changes in conductance variances. We then used these measurements to constrain
the patterns of random release at excitatory and inhibitory synapses in computational models. We show that
measurements of the mean and variance of conductances are sufficient constraints to design models fully
consistent with <em>in vivo</em> recordings.</p>
