<h3><span>Abstract</span></h3>

<p>In the intact cortical network, neurons process information while receiving thousands of synaptic inputs. We
investigated how this noise modulates the ability to resolve constant and oscillatory stimulation by using
simplified neuron models. The cellular response was precise and reliable for a broad range of model parameters,
while the output rate depended on the stimulus amplitude. The results suggest that both temporal and rate
coding schemes are not exclusive but rather encode different aspects of the input at the same time. Conductance
noise was found to play a modulating role, suggesting that high-conductance noise confers advantageous
computational properties to neurons.</p>
