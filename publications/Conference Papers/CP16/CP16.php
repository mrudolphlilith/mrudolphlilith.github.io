<h3><span>Abstract</span></h3>

<p>The immense computational and adaptive power of biological neural systems emerges from the collective dynamics of large
populations of interacting neurons. Thus, for theoretical investigations, optimal strategies for modeling biophysically
faithful neuronal dynamics are required. Here, we propose an extension of the classical leaky integrate-and-fire neuronal
model, the gIF model. It incorporates various aspects of high-conductance state dynamics typically seen in cortical
neurons <em>in vivo</em>, as well as activity-dependent modulation of synaptic weights. The analytic description of the
resulting neuronal models allows their use together with the event-driven simulation strategy. The latter provides
an efficient tool for exact simulations of large-scale neuronal networks.</p>
