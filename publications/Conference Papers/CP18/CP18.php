<h3><span>Abstract</span></h3>

<p>During activated states <em>in vivo</em>, neocortical neurons are subject to intense synaptic activity and high-amplitude
membrane potential (Vm) fluctuations. These 'high-conductance' states may strongly affect the integrative properties of
cortical neurons. We investigated the responsiveness of cortical neurons during different states using a combination
of computational models and <em>in vitro</em> experiments (dynamic-clamp) in the visual cortex of adult guinea-pigs. Spike
responses were monitored following stochastic conductance injection in both experiments and models. We found that
cortical neurons can operate in a continuum between two different modes: during states with equal excitatory and
inhibitory conductances, the firing is mostly correlated with an increase in excitatory conductance, which is a
rather classic scenario. In contrast, during states dominated by inhibition, the firing is mostly related to a
decrease in inhibitory conductances (dis-inhibition). This model prediction was tested experimentally using
dynamic-clamp, and the same modes of firing were identified. We also found that the signature of spikes evoked
by dis-inhibition is a transient drop of the total membrane conductance prior to the spike, which is typical
of states with dominant inhibitory conductances. Such a drop should be identifiable from intracellular recordings
<em>in vivo</em>, which would provide an important test for the presence of inhibition-dominated states. In
conclusion, we show that in artificial activated states, not only inhibition can determine the conductance
state of the membrane, but inhibitory inputs may also have a determinant influence on spiking. Future analyses
and models should focus on verifying if such a determinant influence of inhibitory conductance dynamics
is also present <em>in vivo</em>.</p>
