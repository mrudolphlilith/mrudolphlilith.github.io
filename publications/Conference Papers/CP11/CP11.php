<h3><span>Abstract</span></h3>

<p>We outline an approximation for obtaining an analytic expression of the power spectral density (PSD) of
the membrane potential (Vm) in neurons subject to synaptic noise. In high-conductance states, there is
a remarkable agreement between this approximation and PSDs computed numerically. This analytic expression
can be used to predict how the PSDcdepends on the exact kinetic model for synaptic currents, as well
as on the values of the rate constants. This approach can therefore yield methods to estimate the
characteristics of the kinetics of individual synaptic conductances from the analysis of the Vm
activity in intracellular recordings <em>in vivo</em>.</p>
