<h3><span>Abstract</span></h3>

<p>Cortical neurons <em>in vivo</em> show a highly irregular spontaneous discharge activity, characterized by a gamma
statistics and coefficient of variation around unity. Modelling studies showed that this irregularity is a consequence
of the high-conductance state caused by the ongoing activityin the cortical network. Here, we investigate to
which extent this high discharge variability can be reproduced <em>in vitro</em> using current noise injection. In
agreement with numerical studies, we found that equalizing the time constant of the noisy input with the
membrane time constant may lead to an irregular discharge activity which, however, departs from a gamma
statistics.</p>
