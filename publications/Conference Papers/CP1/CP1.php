<h3><span>Abstract</span></h3>

<p>We discuss some aspects of our programme of investigating gauge theories (with fermions) in terms of local
gauge invariant quantities. In the first part, the functional integral for quantum electrodynamics is
discussed within our formulation. Next, the algebra of Grassmann-algebra-valued invariants for one-flavour
chromodynamics is investigated and, finally, the functional integral for this theory is derived within our
framework.</p>
