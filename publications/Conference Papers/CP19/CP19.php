<h3><span>Abstract</span></h3>

<p>Despite a steady improvement of computational hardware, results of numerical simulation are still tightly bound to the
simulation tool and strategy used, and may substantially vary across available simulation tools or for different settings
within the same simulator. Clock-driven simulation strategies proved efficient for large and highly active networks
but are outperformed with respect to precision by the recently introduced event-driven strategies. Focusing on most
commonly used clock-driven and event-driven approaches, in this paper we evaluate to which extent the temporal
precision of spiking events impacts on neuronal dynamics of single as well as small networks of IF neurons with
plastic synapses. We find that the used strategy can severely alter simulated neural dynamics and, therefore,
turn out to be crucial for the interpretation of the result of numerical simulations. Drastic differences were
observed in models with spike timing dependent plasticity, arguing that the speed of neuronal simulations should
not be the sole criteria for evaluation of the efficiency of simulation tools, but must complement an evaluation
of their exactness, possibly in disfavour of their speed.</p>
