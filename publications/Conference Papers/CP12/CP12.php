<h3><span>Abstract</span></h3>

<p>Neurons in cerebral cortex are characterized by a stochastic subthreshold membrane potential (Vm) activity, which
originates from the ongoing and irregular activity of neurons in the cortical network. Here, we propose a way of
characterizing this network activity by using the notion of shot noise applied to pulse-based kinetic models of
synaptic conductances. We link the statistical characterization of synaptic conductances in terms of their mean
and variance to the activity in the network, in particular the average firing rate of presynaptic neurons and
their temporal correlation, thus providing a possible method for characterizing cortical network activity from
intracellularly recorded Vm activity.</p>
