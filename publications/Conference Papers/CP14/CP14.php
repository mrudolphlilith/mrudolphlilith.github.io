<h3><span>Abstract</span></h3>

<p>In neocortical neurons, network activity is responsible for intense synaptic inputs, which maintain the membrane
in a high-conductance state. Here, we propose a method for recreating specific high-conductance states intracellularly.
This method makes use of the estimation of the mean and variance of excitatory and inhibitory conductances based
on intracellular recordings, and of the injection of appropriate stochastic conductances in <em>in vitro</em> slice preparations
using a dynamic-clamp protocol. The approach could be used to evaluate the modulation of neuronal responses
by specific network states.</p>
