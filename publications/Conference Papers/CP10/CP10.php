<h3><span>Abstract</span></h3>

<p>Cortical neurons <em>in vivo</em> are subject to intense synaptic noise that has a significant impact on various
electrophysiological properties. Here we characterize the subthreshold activity of cortical neurons using an
explicit solution of the passive membrane equation subject to independent inhibitory and excitatory conductance
noise sources described by stochastic random-walk processes. The analytic expression for the membrane potential
distribution can be used to estimate the average and variance of synaptic conductances from intracellular
recordings obtained under current clamp. We demonstrate the application of this method to neuronal models
of various complexity as well as to <em>in vitro</em> intracellular recordings.</p>
