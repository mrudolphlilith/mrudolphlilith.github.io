<h3><span>Abstract</span></h3>

<p>Neocortical neurons <em>in vivo</em> operate in a high-conductance state characterized by an intense intracellular
fluctuating activity. Here we investigate how these conditions impact on the dendritic integration of synaptic
inputs by using biophysical models of morphologically-reconstructed neocortical pyramidal neurons. We find that
the combined effect of high conductance and fluctuating activity due to the synaptic background activity may
set pyramidal neurons into an integrative mode which is determined by the intensity of network activity, which
is fast-conducting and in which the impact of inputs is roughly location-independent.</p>
