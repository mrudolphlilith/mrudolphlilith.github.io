<h3><span>Abstract</span></h3>

<p>During intense network activity, neocortical neurons are in a 'high-conductance' state. To estimate the respective
contributions of excitatory and inhibitory conductances in generating such states, we combined computational
models with intracellular recordings obtained in cat parietal cortex <em>in vivo</em>. Fitting a fluctuating-conductance
model to the recordings revealed that inhibitory conductances are dominant (several times larger than excitation).
Conductance variance (i.e., the 'noise') was also larger for inhibition, indicating that inhibitory dynamics
has a pronounced impact on membrane potential fluctuations. We conclude that the synaptic bombardment of
neocortical neurons <em>in vivo</em> is not excitatory, but mostly determined by inhibitory conductances.</p>
