<h3><span>Abstract</span></h3>

<p>The brain can be thought of as a collective ensemble ranging in the spatial domain from microscopic elements (molecules, 
receptors, ionic channels, synapses) to macroscopic entities (layers, nuclei, cortical areas, neural networks). 
The same multi-scale analysis can be replicated in the temporal domain, when decomposing brain activity in a multitude 
of dynamic processes with time constants ranging from microseconds (molecule transconformation, channel opening) to 
years (postnatal cell replacement, for example in the bird song system; long-term memories, for example in vertebrate 
hippocampus). A tantalising challenge to the field of system and computational neuroscience is to bind in a coherent 
way these different hierarchies of organisation on the basis of experimentally defined descriptors, each of which is 
endowed with a specific spatio-temporal domain and measurement precision.</p>
