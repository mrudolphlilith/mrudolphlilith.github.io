<h3><span>Abstract</span></h3>

<p>In the cerebral cortex of awake animals, neurons are subject to a tremendous fluctuating activity mostly of synaptic
origin and termed 'synaptic noise'. Synaptic noise is the dominant source of membrane potential fluctuations in neurons
and can have a strong influence on their integrative properties. We review here the experimental measurements of
synaptic noise, and its modeling by conductance-based stochastic processes. We next review the consequences of
synaptic noise on neuronal integrative properties, as predicted by computational models and investigated experimentally
using dynamic-clamp. We also review analysis methods, such as spike-triggered average or conductance analysis,
which are derived from the modeling of synaptic noise by stochastic processes. These different approaches aim
at understanding the integrative properties of neocortical neurons in the intact brain.</p>
