<h3><span>Abstract</span></h3>

<p>This paper presents a study of convergence modalities in a small adaptive network of conductance-based neurons, receiving 
input patterns with different degrees correlation . The models for the neurons, synapses and plasticity rules (STDP) have a 
common biophysics basis. The neural network is simulated using a mixed analog-digital platform, which performs real-time 
simulations. We describe the study context, and the models for the neurons and for the adaptation functions. Then we present 
the simulation platform, including analog integrated circuits to simulate the neurons and a real-time software to simulate the 
plasticity. We also detail the analysis tools used to evaluate the final state of the network by the way of its post-adaptation 
synaptic weights. Finally, we present experimental results, with a systematic exploration of the network convergence when varying 
the input correlation, the initial weights and the distribution of hardware neurons to simulate the biological variability.</p>
