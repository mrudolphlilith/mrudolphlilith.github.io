<h3><span>Abstract</span></h3>

<p>While dendritic processing has been well characterized <em>in vitro</em>, there is little experimental 
data and models available about the integrative properties of dendrites <em>in vivo</em>. Here, we review 
computational models which goal is to infer the dendritic processing of neocortical neurons and dendrites 
<em>in vivo</em>. We start by summarizing experimental measurements of the "high-conductance states" of 
cortical neurons <em>in vivo</em>. Next, we show models predicting that, in such states, the responsiveness 
of cortical neurons should be greatly enhanced, in particular due to the presence of high-amplitude fluctuations
("synaptic noise").  We infer that in dendrites, this effect should be very strong, leading to the spontaneous 
activation of dendritic spikes. The presence of noise in dendrites also enhances spike propagation.  We show 
that opposite distance-dependences of spike initiation and propagation result in roughly location-independent 
synaptic efficacies.  In addition, in high-conductance states, dendrites display sharper temporal processing 
capabilities.  Thus, we conclude that noisy active dendrites behave more "democratically", and that dendrites 
should have enhanced processing capabilities <em>in vivo</em>.</p>
