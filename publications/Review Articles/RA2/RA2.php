<h3><span>Abstract</span></h3>

<p>During intense network activity <em>in vivo</em>, cortical neurons are in a high-conductance state, 
in which the membrane potential (Vm) is subject to a tremendous fluctuating activity. Clearly, this 'synaptic 
noise' contains information about the activity of the network, but there are presently no methods available 
to extract this information. We focus here on this problem from a computational neuroscience perspective,
with the aim of drawing methods to analyze experimental data. We start from models of cortical neurons,
in which high-conductance states stem from the random release of thousands of excitatory and inhibitory
synapses. This highly complex system can be simplified by using global synaptic conductances described
by effective stochastic processes. The advantage of this approach is that one can derive analytically
a number of properties from the statistics of resulting Vm fluctuations. For example, the global excitatory
and inhibitory conductances can be extracted from synaptic noise, and can be related to the mean activity
of presynaptic neurons. We show here that extracting the variances of excitatory and inhibitory synaptic
conductances can provide estimates of the mean temporal correlation-or level of synchrony-among thousands
of neurons in the network. Thus, 'probing the network' through intracellular Vm activity is possible and
constitutes a promising approach, but it will require a continuous effort combining theory, computational
models and intracellular physiology.</p>
