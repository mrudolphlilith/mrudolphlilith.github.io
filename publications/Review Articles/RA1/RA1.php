<h3><span>Abstract</span></h3>

<p>Intracellular recordings <em>in vivo</em> have shown that neocortical neurons are subjected to an intense
synaptic bombardment in intact networks, and are in a 'high-conductance' state. <em>In vitro</em> studies have shed
light on the complex interplay between the active properties of dendrites and how they convey discrete synaptic
inputs to the soma. Computational models have attempted to tie these results together and predicted that
high-conductance states profoundly alter the integrative properties of cortical neurons, providing them
with a number of computational advantages. Here, we summarize results from these different approaches,
with the aim of understanding the integrative properties of neocortical neurons in the intact brain.</p>

