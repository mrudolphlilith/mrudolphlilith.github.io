<h3><span>Abstract</span></h3>

<p>The slow (<1 Hz) oscillation, with its alternating 'up' and 'down' states inindividual neurons, is a defining feature
of the electroencephalogram (EEG) during slow-wave sleep (SWS). Although this oscillation is well preserved across
mammalian species, its physiological role is unclear. Electrophysiological and computational evidence from the
cortex and thalamus now indicates that slow-oscillation 'up' states and the 'activated' state of wakefulness are
remarkably similar dynamic entities. This is consistent with behavioural experiments suggesting that slow-oscillation
'up' states provide a context for the replay, and possible consolidation, of previous experience. In this scenario,
the T-type Ca<sup>2+</sup> channel-dependent bursts of action potentials that initiate each 'up' state in thalamocortical
(TC) neurons might function as triggers for synaptic and cellular plasticity in corticothalamic networks.</p>
