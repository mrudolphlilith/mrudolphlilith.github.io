<h3><span>Abstract</span></h3>

<p>Cortical neurons are subject to sustained and irregular synaptic activity which causes important fluctuations
of the membrane potential (Vm). We review here different methods to characterize this activity and its impact
on spike generation. The simplified, fluctuating point-conductance model of synaptic activity provides the
starting point of a variety of methods for the analysis of intracellular Vm recordings. In this model, the
synaptic excitatory and inhibitory conductances are described by Gaussian-distributed stochastic variables,
or colored conductance noise. The matching of experimentally recorded Vm distributions to an invertible
theoretical expression derived from the model allows the extraction of parameters characterizing the synaptic
conductance distributions. This analysis can be complemented by the matching of experimental Vm power
spectral densities (PSDs) to a theoretical template, even though the unexpected scaling properties of
experimental PSDs limit the precision of this latter approach. Building on this stochastic characterization
of synaptic activity, we also propose methods to qualitatively and quantitatively evaluate spike-triggered
averages of synaptic time-courses preceding spikes. This analysis points to an essential role for synaptic
conductance variance in determining spike times. The presented methods are evaluated using controlled
conductance injection in cortical neurons <em>in vitro</em> with the dynamic-clamp technique. We review their
applications to the analysis of <em>in vivo</em> intracellular recordings in cat association cortex, which suggest
a predominant role for inhibition in determining both sub- and supra-threshold dynamics of cortical neurons
embedded in active networks.</p>
